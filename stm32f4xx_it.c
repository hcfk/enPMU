/**
* @brief This function handles System tick timer.
*/
void SysTick_Handler(void)
{
  /* USER CODE BEGIN SysTick_IRQn 0 */

  /* USER CODE END SysTick_IRQn 0 */
  HAL_IncTick();
  HAL_SYSTICK_IRQHandler();

	rtu_modbusReceiveTimeout();
	pmu_timeProcess();
	
	if (demandParams.runTimeFlag == ACTIVE)
	{
		if (demandParams.timerCnt == 0) pmu_getFirstEnergyIndex();
		demandParams.timerCnt++;
		if (demandParams.timerCnt >= DEMAND_TIME)
		{
			pmu_getSecondEnergyIndex();
			pmu_calculateDemand();
		}
	}
	
  /* USER CODE BEGIN SysTick_IRQn 1 */

  /* USER CODE END SysTick_IRQn 1 */
}

