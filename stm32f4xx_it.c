/**
* @brief This function handles EXTI line[9:5] interrupts.
*/
void EXTI9_5_IRQHandler(void)
{
  /* USER CODE BEGIN EXTI9_5_IRQn 0 */
	deviceParams.adcFailCounter = 0;
	deviceParams.dataReadyFlag = 1;
	deviceParams.adcSuccess++;
	if(deviceParams.adcSuccess == 10000) deviceParams.adcSuccess = 0;
	adc_readVoltageADC();
	adc_readCurrentADC1();
	adc_readCurrentADC2();
    pmu_signalShift();
    pmu_signalShift_powerCalculation();
	spectra();
  /* USER CODE END EXTI9_5_IRQn 0 */
  HAL_GPIO_EXTI_IRQHandler(GPIO_PIN_8);
  /* USER CODE BEGIN EXTI9_5_IRQn 1 */


  /* USER CODE END EXTI9_5_IRQn 1 */
}

